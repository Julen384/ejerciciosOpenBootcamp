// Sentencias Switch

let nota = 0;

switch (nota){
    case 5:
        console.log("Buen trabajo, ¡Sobresaliente!");
        break;
    case 4:
        console.log("Buen trabajo, pero podrías haberlo hecho mejor.");
        break;
    case 3:
        console.log("Bien, has obtenido un suficiente.");
        break;
    case 2: 
        console.log("No has aprobado, pero por poco.");
        break;
    case 1:
        console.log("No has estudiado nada amijo, esfuerzate más para la próxima.");
        break;
    default:
        console.log("Error")
        break;

}